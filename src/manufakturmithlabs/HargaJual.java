    private void bCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCalculateActionPerformed
        // TODO add your handling code here:
        float hargaJual = 0 ;
        float hpp = 0;
        float margin = 0;
        
        hpp = Float.parseFloat(tHPP.getText());
        margin = Float.parseFloat(tMargin.getText());
        hargaJual = (hpp * margin / 100)+hpp;
        tHargaJual.setText(String.valueOf(hargaJual));
    }//GEN-LAST:event_bCalculateActionPerformed

