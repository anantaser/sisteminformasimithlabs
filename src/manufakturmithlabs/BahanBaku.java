    protected void datatable(){
        Object[] Baris = {"Kode Barang","Nama Barang","Stock","Satuan","Harga Satuan"};
        tabmode = new DefaultTableModel(null,Baris);
        TabBahanBaku.setModel(tabmode);
        
        try{
            sql = "SELECT `kdbarang`, `namabarang`, `stock`, `satuan`, `harga`, `kategori` FROM `bahan` WHERE kategori='bahan baku'";
            rs = stat.executeQuery(sql);
            while(rs.next()){
                String a = rs.getString("kdbarang");
                String b = rs.getString("namabarang");
                String c = rs.getString("stock");
                String d = rs.getString("satuan");
                String e = rs.getString("harga");      
                
                String[] data = {a,b,c,d,e};
                tabmode.addRow(data);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null,"Database Error");
        }
    }

