    private void bAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddActionPerformed
        // TODO add your handling code here:
        try{
            sql = "INSERT INTO `bahan`(`kdbarang`, `namabarang`, `stock`, `satuan`, `harga`, `kategori`) VALUES (?,?,?,?,?,'Bahan Baku');";
            java.sql.PreparedStatement stat = con.prepareStatement(sql);
            stat.setString(1,tKodeBarang.getText());
            stat.setString(2,tNamaBarang.getText());
            stat.setString(3,tStock.getText());            
            stat.setString(4,cSatuan.getSelectedItem().toString());
            stat.setString(5,tHargaSatuan.getText());
            stat.executeUpdate();
            tKodeBarang.requestFocus();
            datatable();
            bersih();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Data Gagal Disimpan");
        }
        
    }//GEN-LAST:event_bAddActionPerformed

